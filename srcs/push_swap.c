#include "../libft/includes/ft_printf.h"
#include "../includes/push_swap.h"

int	main(int argc, char **argv)
{
	t_stack	*a;

	a = ft_process(argc, argv);
	if (!a || ft_checkdup(a))
	{
		ft_free(&a);
		ft_printf("error\n"); 
	}


	if (!ft_checksorted(a)) 
		ft_sort(&a);
	ft_free(&a);
	return (0);
}
